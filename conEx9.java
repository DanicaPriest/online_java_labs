public class conEx9 {
    /**
     * Conditions and Loops Exercise 9: break
     *
     *      Demonstrate the use of the "break" statement to exit a loop.
     *
     */
    public static void main(String[] args){
        int i = 1;
        while(i < 100){
            i++;
                    if (i == 25)
                    {System.out.println("Break time!");
                    break;
            }
        }
    }
}
